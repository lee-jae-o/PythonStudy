def 애너그램_판별(문자열1, 문자열2):
    문자열1 = 문자열1.replace(" ", "").lower()
    문자열2 = 문자열2.replace(" ", "").lower()

    if len(문자열1) != len(문자열2):
        return False

    문자열1_카운트 = {}
    문자열2_카운트 = {}

    for 문자 in 문자열1:
        if 문자 in 문자열1_카운트:
            문자열1_카운트[문자] += 1
        else:
            문자열1_카운트[문자] = 1

    for 문자 in 문자열2:
        if 문자 in 문자열2_카운트:
            문자열2_카운트[문자] += 1
        else:
            문자열2_카운트[문자] = 1

    return 문자열1_카운트 == 문자열2_카운트

if __name__ == "__main__":
    문자열1 = input("첫 번째 문자열을 입력하세요: ")
    문자열2 = input("두 번째 문자열을 입력하세요: ")

    if 애너그램_판별(문자열1, 문자열2):
        print("입력한 두 문자열은 애너그램입니다.")
    else:
        print("입력한 두 문자열은 애너그램이 아닙니다.")
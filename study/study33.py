def 최댓값최솟값찾기(숫자리스트):
    if not 숫자리스트:
        return None, None  # 빈 리스트인 경우 예외 처리

    최댓값 = 최솟값 = 숫자리스트[0]

    for 숫자 in 숫자리스트:
        if 숫자 > 최댓값:
            최댓값 = 숫자
        elif 숫자 < 최솟값:
            최솟값 = 숫자

    return 최댓값, 최솟값

if __name__ == "__main__":
    숫자리스트 = [3, 1, 4, 1, 5, 9, 2, 6, 5, 3, 5]
    최댓값, 최솟값 = 최댓값최솟값찾기(숫자리스트)

    print("원본 숫자 리스트:", 숫자리스트)
    print("최댓값:", 최댓값)
    print("최솟값:", 최솟값)
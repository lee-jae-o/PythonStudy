def 단어_정렬_중복제거(문자열):
    # 입력된 문자열을 공백을 기준으로 단어들로 분리하고 정렬합니다.
    단어들 = sorted(문자열.split())

    # 중복을 제거하여 유일한 단어들의 목록을 얻습니다.
    유일한_단어들 = []
    for 단어 in 단어들:
        if 단어 not in 유일한_단어들:
            유일한_단어들.append(단어)

    return 유일한_단어들

if __name__ == "__main__":
    입력문자열 = input("단어들을 포함한 문자열을 입력하세요: ")
    
    결과 = 단어_정렬_중복제거(입력문자열)
    
    print("정렬된 유일한 단어들 목록:")
    for 단어 in 결과:
        print(단어)
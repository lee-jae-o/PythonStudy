def 간단한계산기():
    print("간단한 계산기에 오신 것을 환영합니다!")
    숫자1 = float(input("첫 번째 숫자를 입력하세요: "))
    연산자 = input("사칙연산 중 하나를 선택하세요 (+, -, *, /): ")
    숫자2 = float(input("두 번째 숫자를 입력하세요: "))

    if 연산자 == "+":
        결과 = 숫자1 + 숫자2
    elif 연산자 == "-":
        결과 = 숫자1 - 숫자2
    elif 연산자 == "*":
        결과 = 숫자1 * 숫자2
    elif 연산자 == "/":
        결과 = 숫자1 / 숫자2
    else:
        print("올바른 연산자를 입력하세요.")
        return

    print(f"결과: {결과}")

if __name__ == "__main__":
    간단한계산기()
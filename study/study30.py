def 문자열문자세기(문자열):
    문자_카운트 = {}

    for 문자 in 문자열:
        if 문자.isalpha():
            문자 = 문자.lower()  # 대소문자를 구분하지 않음
            if 문자 in 문자_카운트:
                문자_카운트[문자] += 1
            else:
                문자_카운트[문자] = 1

    for 문자, 카운트 in 문자_카운트.items():
        print(f"'{문자}': {카운트}번 등장")

if __name__ == "__main__":
    사용자입력 = input("문자열을 입력하세요: ")
    문자열문자세기(사용자입력)
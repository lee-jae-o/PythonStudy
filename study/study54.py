def 가장_많이_등장하는_문자(문자열):
    문자_빈도 = {}
    
    for 문자 in 문자열:
        if 문자.isalpha():  # 알파벳인 경우에만 카운트
            문자 = 문자.lower()  # 대소문자 구분하지 않음
            if 문자 in 문자_빈도:
                문자_빈도[문자] += 1
            else:
                문자_빈도[문자] = 1
    
    최대_빈도 = max(문자_빈도.values())
    가장_많이_등장하는_문자들 = [문자 for 문자, 빈도 in 문자_빈도.items() if 빈도 == 최대_빈도]
    
    return 가장_많이_등장하는_문자들

if __name__ == "__main__":
    입력문자열 = input("문자열을 입력하세요: ")
    결과 = 가장_많이_등장하는_문자(입력문자열)
    
    if 결과:
        print("가장 많이 등장하는 문자:", 결과)
    else:
        print("알파벳 문자가 없습니다.")
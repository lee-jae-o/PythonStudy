def 합이_특정_값인_조합(리스트, 목표_합):
    조합들 = []
    쌍들 = set()

    for 숫자 in 리스트:
        차 = 목표_합 - 숫자
        if 차 in 쌍들:
            조합들.append((숫자, 차))
        쌍들.add(숫자)

    return 조합들

if __name__ == "__main__":
    정수_리스트 = [2, 4, 3, 6, 8, 5, 1, 7]
    목표_합 = 9
    결과 = 합이_특정_값인_조합(정수_리스트, 목표_합)
    print(f"합이 {목표_합}이 되는 모든 조합:", 결과)
